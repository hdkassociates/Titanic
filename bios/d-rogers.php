<p>Training: The Guildhall School (winner of the Characterisation Prize).</p>

<p>Dudley played Isidor Straus in the Southwark Playhouse, Charing Cross Theatre and UK & Ireland touring productions of <i>Titanic</i>.</p>

<p>Theatre includes: Geppetto in <i>Pinocchio</i> (Torch Theatre, Milford Haven); <i>Cinderella</i> (Harlow Playhouse); <i>A Catered Affair</i>, <i>Parade</i> (London Theatre Workshop); <i>Bedroom Farce</i> (Wales tour); <i>Santa Claus And The Christmas Adventure</i> (Epsom Playhouse); <i>Three Sisters</i> (Southwark Playhouse); the UK première of <i>Kander and Ebb’s Curtains</i> (Landor Theatre); <i>Remains Of The Day, 1888</i> (Union Theatre); Dr. Wheedon in <i>Beyond Reasonable Doubt</i>, Mr Allworthy in <i>Tom Jones</i> (UK tours); Mushnik in <i>Little Shop Of Horrors</i>, Cannon Throbbing in <i>Habeus Corpus</i>, Eric Younglove in <i>Privates On Parade</i> (Milford Haven); <i>Miss Watson’s Music Hall</i> (Watermill Theatre); <i>Blood Brothers</i>, Fagin in <i>Oliver!</i> (Brunton Theatre Company); original cast of <i>Joseph And The Amazing Technicolor Dreamcoat</i> (Albery Theatre); <i>Irma La Douce</i> (Shaftesbury Theatre); original cast of <i>Fiddler On The Roof</i> (Her Majesty’s Theatre); Scrooge in <i>A Christmas Carol</i> (Geneva).
</p>

<p>Dudley has played several panto Dames and has also sailed round the world three times as a member of the Stadium Theatre Company on board S.S.Canberra.</p>

<p>Film and television includes:  Taboo with <i>Tom Hardy</i> (BBC); <i>Take A Girl Like You</i> directed by Jonathan Miller (British Lion); <i>Thank You Miss Jones</i> (Thames Television); the interviewer from hell in the Bacardi Breezer commercial.</p>

