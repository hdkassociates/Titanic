<p>Training: Royal Scottish Academy Of Music And Drama.</p>

<p>Kieran played Murdoch in the UK & Ireland touring production of <i>Titanic</i>.</p>

<p>Theatre includes: u/s Phantom in <i>The Phantom Of The Opera</i> (Her Majesty’s Theatre); Elphaba’s Father and u/s Dillamond and Wizard in <i>Wicked</i> (Apollo Victoria); u/s Raoul in <i>Love Never Dies</i> (Adelphi Theatre); Feuilly in <i>Les Misérables</i> (Palace Theatre & O2 Arena); Doc in the European première of <i>Pipe Dream</i> (Union Theatre); Phantom in UK première of Maury Yeston’s <i>Phantom</i> (Rose & Crown Theatre); The Actor in <i>The Woman In Black</i>, Algernon Moncreiff in <i>The Importance Of Being Earnest</i>, <i>Don’t Dress For Dinner, Beyond Therapy</i> (International Theatre, Vienna); <i>Three Phantoms</i> (Macau & Singapore).</p>

<p>Cabaret and concerts include: <i>Scots In The City</i> (Above The Arts & The Other Palace); <i>My Big Fat MUSICAL Cabaret</i> (Union Theatre); <i>Once Upon A Song</i> (Lauderdale House); <i>Music That Matters</i> (Union Theatre & Drachengasse Theatre, Vienna); <i>West End Winters & West End Spring - Switched</i> (Theatre Drachengasse, Vienna); <i>West End To Broadway</i> (English Theatre Of Vienna); <i>Scott Alan New Year’s Concert Live At Zedel</i> (Crazy Coqs); <i>A Touch Of West End</i> (Woburn); <i>Three Witches</i> (Shelley Theatre, Bournemouth).</p>

